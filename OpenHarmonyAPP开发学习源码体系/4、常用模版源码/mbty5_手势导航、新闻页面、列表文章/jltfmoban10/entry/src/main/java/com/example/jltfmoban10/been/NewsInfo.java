package com.example.jltfmoban10.been;

/**
 * News information
 */
public class NewsInfo {
    /**
     * news title
     */
    private String title;

    /**
     * news type
     */
    private String type;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
